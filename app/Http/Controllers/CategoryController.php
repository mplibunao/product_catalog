     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store()
    {
        $this->validate(request(), [
            'name' => 'required',
            'description' => 'required',
            'image' => 'url'
        ]);

        Category::create([
            'name' => request('name'),
            'description' => request('description'),
            'image' => request('image')
        ]);

        return redirect('/admin');
    }

